        String currentRes = parentFrame.getWidth() + "x" + parentFrame.getHeight();

        boolean found = false;
        for (String r : resolutions) { if (r.equals(currentRes)) { found = true; break; } }
        if (found) resCombo.setSelectedItem(currentRes);

